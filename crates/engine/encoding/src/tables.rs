#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn win1251_uppercase_range_is_linear() {
        // 0xC0 (index 64) → 'А' (U+0410), и далее по алфавиту до 0xDF → 'Я'.
        for i in 0..32u8 {
            assert_eq!(WIN1251[(64 + i) as usize] as u32, 0x0410 + u32::from(i));
        }
    }

    #[test]
    fn win1251_lowercase_range_is_linear() {
        // 0xE0 (index 96) → 'а' (U+0430), и далее по алфавиту до 0xFF → 'я'.
        for i in 0..32u8 {
            assert_eq!(WIN1251[(96 + i) as usize] as u32, 0x0430 + u32::from(i));
        }
    }

    #[test]
    fn win1251_yo_pair() {
        // Ё / ё — отдельная пара, не в основном диапазоне.
        assert_eq!(WIN1251[(0xA8 - 0x80) as usize], 'Ё');
        assert_eq!(WIN1251[(0xB8 - 0x80) as usize], 'ё');
    }

    #[test]
    fn win1251_undefined_is_replacement() {
        // 0x98 — единственная неопределённая позиция в WHATWG-таблице.
        assert_eq!(WIN1251[(0x98 - 0x80) as usize], '\u{FFFD}');
    }

    #[test]
    fn koi8r_phonetic_layout() {
        // KOI8-R: 'а' = 0xC1 (а не 0xE0 как в cp1251/cp866).
        assert_eq!(KOI8_R[(0xC1 - 0x80) as usize], 'а');
        assert_eq!(KOI8_R[(0xD7 - 0x80) as usize], 'в');
        assert_eq!(KOI8_R[(0xE1 - 0x80) as usize], 'А');
    }

    #[test]
    fn koi8r_yo_pair() {
        assert_eq!(KOI8_R[(0xB3 - 0x80) as usize], 'Ё');
        assert_eq!(KOI8_R[(0xA3 - 0x80) as usize], 'ё');
    }

    #[test]
    fn cp866_uppercase_block_one() {
        // 0x80..=0x8F → А..П.
        for i in 0..16u8 {
            assert_eq!(CP866[i as usize] as u32, 0x0410 + u32::from(i));
        }
    }

    #[test]
    fn cp866_lowercase_split() {
        // 0xA0..=0xAF → а..п, потом псевдографика, потом 0xE0..=0xEF → р..я.
        for i in 0..16u8 {
            assert_eq!(CP866[(0xA0 - 0x80 + i) as usize] as u32, 0x0430 + u32::from(i));
        }
        for i in 0..16u8 {
            assert_eq!(
                CP866[(0xE0 - 0x80 + i) as usize] as u32,
                0x0440 + u32::from(i)
            );
        }
    }

    #[test]
    fn cp866_yo_pair() {
        assert_eq!(CP866[(0xF0 - 0x80) as usize], 'Ё');
        assert_eq!(CP866[(0xF1 - 0x80) as usize], 'ё');
    }
}
